#ifndef __LIB_STDINT_H
#define __LIB_STDINT_H
typedef signed char int8_t;           // signed char 类型一个字节 8位
typedef signed short int int16_t;     // signed short in 2个字节 18位
typedef signed int int32_t;           // signed int 4个字节 32 位
typedef signed long long int int64_t; // 以此类推
typedef unsigned char uint8_t;
typedef unsigned short int uint16_t;
typedef unsigned int uint32_t;
typedef unsigned long long int uint64_t;
#endif
